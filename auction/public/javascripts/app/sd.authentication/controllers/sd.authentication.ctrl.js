(function () {
    'use strict';

    angular
        .module('sd.authentication')
        .controller('sd.authentication.ctrl', Controller);

    /** @ngInject */

    function Controller($scope, $state, auth) {
        $scope.user = {};

        $scope.register = function () {
            auth.register($scope.user).error(function (error) {
                $scope.error = error;
            }).then(function () {
                $state.go('home');
            });
        };

        $scope.login = function () {
            auth.logIn($scope.user).error(function (error) {
                $scope.error = error;
            }).then(function () {
                $state.go('home');
            });
        };

    }
})();
